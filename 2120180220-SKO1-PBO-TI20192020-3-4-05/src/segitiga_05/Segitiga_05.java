package segitiga_05;
public class Segitiga_05 {
    public static void main(String[] args) {
    for (int i=1;i<=10;i++){
             for(int j=1;j<=i;j++){
                 System.out.print(" ");
             }
             for(int z=10;z>=i;z--){
              System.out.print("5");
              }
             for(int k=9;k>=i;k--){
               System.out.print("5");
               }
             System.out.println("");
         }
 }

}
